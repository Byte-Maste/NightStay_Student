package com.codewithkrishnachoudhary;

public class POJO {
    private String name;
    private String password;

    public POJO(String name, String password) {
        this.name = name;
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }
}
